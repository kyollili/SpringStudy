package com.sist.jejubest;

public class GoodsVO {
  private int no;
  private String name;
  private String poster;
  private String send1;
  private String send2;
  private String price;
  private String sendPrice;
public int getNo() {
	return no;
}
public void setNo(int no) {
	this.no = no;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getPoster() {
	return poster;
}
public void setPoster(String poster) {
	this.poster = poster;
}
public String getSend1() {
	return send1;
}
public void setSend1(String send1) {
	this.send1 = send1;
}
public String getSend2() {
	return send2;
}
public void setSend2(String send2) {
	this.send2 = send2;
}
public String getPrice() {
	return price;
}
public void setPrice(String price) {
	this.price = price;
}
public String getSendPrice() {
	return sendPrice;
}
public void setSendPrice(String sendPrice) {
	this.sendPrice = sendPrice;
}
  
}
